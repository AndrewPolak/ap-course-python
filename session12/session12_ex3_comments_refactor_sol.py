def main():
    # This script calculates the sum of squares of two numbers.

    num1 = 3
    num2 = 4

    # square both
    square1 = num1 ** 2
    square2 = num2 ** 2

    # sum them
    total = square1 + square2

    # print result
    print(f"Sum of squares: {total}")

if __name__ == "__main__":
    main()